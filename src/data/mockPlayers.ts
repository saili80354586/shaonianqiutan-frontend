// 按地区分组统计
export const getPlayersByProvince = (): Record<string, Player[]> => {
  const grouped: Record<string, Player[]> = {};
  mockPlayers.forEach(player => {
    if (!grouped[player.province]) {
      grouped[player.province] = [];
    }
    grouped[player.province].push(player);
  });
  return grouped;
};

// 获取统计数据
export const getPlayerStats = () => {
  const totalPlayers = mockPlayers.length;
  const ageDistribution = {
    'U11': mockPlayers.filter(p => p.age === 11).length,
    'U12': mockPlayers.filter(p => p.age === 12).length,
    'U13': mockPlayers.filter(p => p.age === 13).length,
    'U14': mockPlayers.filter(p => p.age === 14).length,
  };
  
  const positionDistribution = {
    'GK': mockPlayers.filter(p => p.position === 'GK').length,
    'DEF': mockPlayers.filter(p => ['CB', 'LB', 'RB'].includes(p.position || '')).length,
    'MID': mockPlayers.filter(p => ['CM', 'DM', 'AM', 'LM', 'RM', 'CAM'].includes(p.position || '')).length,
    'FWD': mockPlayers.filter(p => ['ST', 'CF', 'LW', 'RW'].includes(p.position || '')).length,
  };
  
  const faRegisteredCount = mockPlayers.filter(p => p.faRegistered).length;
  
  // 技术标签热度统计
  const tagCount: Record<string, number> = {};
  mockPlayers.forEach(p => {
    p.technicalTags?.forEach(tag => {
      tagCount[tag] = (tagCount[tag] || 0) + 1;
    });
  });
  const topTechnicalTags = Object.entries(tagCount)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 10);
  
  // 体测平均值
  const avgPhysicalTests = {
    sprint30m: average(mockPlayers.map(p => p.physicalTests?.sprint30m).filter(Boolean) as number[]),
    longJump: average(mockPlayers.map(p => p.physicalTests?.longJump).filter(Boolean) as number[]),
    flexibility: average(mockPlayers.map(p => p.physicalTests?.flexibility).filter(Boolean) as number[]),
    pullUps: average(mockPlayers.map(p => p.physicalTests?.pullUps).filter(Boolean) as number[]),
  };
  
  return {
    totalPlayers,
    ageDistribution,
    positionDistribution,
    faRegisteredCount,
    topTechnicalTags,
    avgPhysicalTests,
  };
};

const average = (arr: number[]): number => {
  if (arr.length === 0) return 0;
  return Math.round((arr.reduce((a, b) => a + b, 0) / arr.length) * 10) / 10;
};

export default mockPlayers;
